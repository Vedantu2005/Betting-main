import React, { useState } from "react";
import { X } from "lucide-react";
import { signInWithEmailAndPassword } from "firebase/auth";
import { auth } from "../config/firebase"; // Kept exact path as requested

export default function Login({ onClose, onSignupClick, onForgotPassword }) {
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState("");

  const handleLogin = async () => {
    setError("");
    if (!email || !password) {
      setError("Please fill in all fields.");
      return;
    }

    setLoading(true);
    try {
      await signInWithEmailAndPassword(auth, email, password);
      // Login successful - the App's auth listener will handle the redirect/close
      onClose(); 
    } catch (err) {
      console.error("Login Error:", err);
      if (err.code === "auth/invalid-credential" || err.code === "auth/wrong-password") {
        setError("Invalid email or password.");
      } else if (err.code === "auth/user-not-found") {
        setError("No account found with this email.");
      } else if (err.code === "auth/too-many-requests") {
        setError("Too many failed attempts. Please try again later.");
      } else {
        setError("Login failed. Please try again.");
      }
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="fixed inset-0 flex justify-center items-center bg-black/60 backdrop-blur-sm z-50 p-4">
      <div className="flex flex-col md:flex-row rounded-[37px] md:rounded-[37px] overflow-hidden shadow-2xl bg-white w-full max-w-[1108px] max-h-[90vh] md:h-[625px]">

        {/* Left Side - Visuals */}
        <div className="w-full md:w-1/2 bg-blue-600 flex flex-col justify-center items-start text-white p-6 md:p-8 text-start relative">
          <button
            onClick={onClose}
            className="absolute cursor-pointer top-4 right-4 text-white/80 hover:text-white block md:hidden"
          >
            <X size={20} />
          </button>

          <h2 className="font-inter font-bold uppercase text-xl md:text-[26px] leading-[30px] md:leading-[40px] tracking-normal mb-2">
            <span className="block md:hidden">Predict, Bet, and Win</span>
            <span className="hidden md:block">Maximize Your Winnings <br />
              With Expert Cricket Insights.</span>
          </h2>

          <p className="font-inter font-medium text-sm md:text-[15px] leading-[20px] md:leading-[29.16px] text-blue-100 mb-4 md:mb-6 tracking-normal">
              Welcome back! Log in to access top-rated betting apps and exclusive odds.
          </p>

          <img
            src="/freebet-bonus.png"
            alt="Login Visual"
            className="hidden md:block object-contain w-full max-w-[577px] h-auto max-h-[390px] drop-shadow-xl"
          />
        </div>

        {/* Right Side - Login Form */}
        <div className="w-full md:w-1/2 bg-white text-gray-900 p-6 md:p-8 relative overflow-y-auto flex-1 flex flex-col justify-center">
          <button
            onClick={onClose}
            className="absolute cursor-pointer top-6 right-6 text-gray-400 hover:text-gray-900 transition-colors hidden md:block"
          >
            <X size={24} />
          </button>

          <div className="mb-4 md:mb-6">
            <p className="text-gray-500 text-sm mb-2">Welcome Back</p>
            <h2 className="text-2xl md:text-3xl font-bold text-gray-900">Log In</h2>
          </div>

          <div className="space-y-6">
            {error && (
              <div className="bg-red-50 border border-red-200 text-red-600 text-sm p-3 rounded-lg text-center">
                {error}
              </div>
            )}

            {/* Email Input */}
            <div className="relative w-full group">
              <label className="absolute -top-3 left-4 bg-white px-1 text-sm text-blue-600 font-medium">
                Email address
              </label>
              <input
                type="email"
                value={email}
                onChange={(e) => { setEmail(e.target.value); setError(""); }}
                placeholder="Enter your email"
                className="w-full px-4 py-3 text-center bg-white border border-gray-300 rounded-full text-gray-900 placeholder-gray-400 focus:outline-none focus:border-blue-600 focus:ring-1 focus:ring-blue-600 transition-all"
              />
            </div>

            {/* Password Input */}
            <div className="relative w-full group">
              <label className="absolute -top-3 left-4 bg-white px-1 text-sm text-blue-600 font-medium">
                Password
              </label>
              <input
                type="password"
                value={password}
                onChange={(e) => { setPassword(e.target.value); setError(""); }}
                placeholder="Enter password"
                className="w-full px-4 py-3 text-center bg-white border border-gray-300 rounded-full text-gray-900 placeholder-gray-400 focus:outline-none focus:border-blue-600 focus:ring-1 focus:ring-blue-600 transition-all"
              />
            </div>

            {/* Forgot Password Link */}
            <div className="flex justify-end">
              <button 
                onClick={onForgotPassword}
                className="text-sm text-gray-500 hover:text-blue-600 transition-colors font-medium"
              >
                Forgot Password?
              </button>
            </div>

            {/* Login Button */}
            <button
              onClick={handleLogin}
              disabled={loading}
              className="w-full py-3 bg-blue-600 text-white cursor-pointer font-bold rounded-full hover:bg-blue-700 transition-all shadow-md hover:shadow-lg disabled:opacity-70 disabled:cursor-not-allowed"
            >
              {loading ? "Logging in..." : "Log In"}
            </button>
          </div>

          <div className="text-center mt-8">
            <span className="text-gray-500">New here? </span>
            <button
              onClick={() => { onClose(); onSignupClick(); }}
              className="text-blue-600 font-bold cursor-pointer hover:text-blue-800 hover:underline transition-colors"
            >
              Sign up
            </button>
          </div>
        </div>
      </div>
    </div>
  );
}